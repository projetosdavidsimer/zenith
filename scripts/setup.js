// CONFIGURA√á√ÉO DO DOCKER
// ==============================================

async function setupDocker() {
  log.title('Configurando Docker');

  try {
    // Verificar se Docker est√° rodando
    execSync('docker info', { stdio: 'ignore' });
    log.success('Docker est√° rodando');

    const startDocker = await question('Deseja iniciar os servi√ßos Docker? (Y/n): ');
    if (startDocker.toLowerCase() !== 'n') {
      log.info('Iniciando servi√ßos Docker...');
      
      // Parar containers existentes
      try {
        execSync('docker-compose down', { stdio: 'ignore' });
      } catch (error) {
        // Ignorar erro se n√£o h√° containers rodando
      }

      // Iniciar servi√ßos
      execSync('docker-compose up -d postgres redis rabbitmq', { stdio: 'inherit' });
      log.success('Servi√ßos Docker iniciados (PostgreSQL, Redis, RabbitMQ)');

      // Aguardar servi√ßos ficarem prontos
      log.info('Aguardando servi√ßos ficarem prontos...');
      await sleep(10000); // 10 segundos
    }
  } catch (error) {
    log.error('Docker n√£o est√° rodando. Por favor, inicie o Docker e tente novamente.');
    process.exit(1);
  }
}

