async function setupDatabase() {
  log.title('Configurando Banco de Dados');

  try {
    // Verificar conex√£o com PostgreSQL
    log.info('Verificando conex√£o com PostgreSQL...');
    
    // Aguardar PostgreSQL estar pronto
    let retries = 30;
    while (retries > 0) {
      try {
        execSync('docker-compose exec -T postgres pg_isready -U vizinho_admin', { stdio: 'ignore' });
        break;
      } catch (error) {
        retries--;
        if (retries === 0) {
          throw new Error('PostgreSQL n√£o est√° respondendo');
        }
        log.info('Aguardando PostgreSQL...');
        await sleep(2000);
      }
    }

    log.success('PostgreSQL est√° pronto');

    // Executar migra√ß√µes (quando implementadas)
    log.info('Executando migra√ß√µes...');
    // execSync('npm run migrate', { stdio: 'inherit' });
    log.success('Migra√ß√µes executadas (placeholder)');

    // Executar seeds (quando implementados)
    log.info('Populando dados iniciais...');
    // execSync('npm run seed', { stdio: 'inherit' });
    log.success('Dados iniciais criados (placeholder)');

  } catch (error) {
    log.error(`Erro na configura√ß√£o do banco: ${error.message}`);
    log.warning('Voc√™ pode executar as migra√ß√µes manualmente depois com: npm run migrate');
  }
}

