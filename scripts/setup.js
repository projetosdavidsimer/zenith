async function checkPrerequisites() {
  log.title('Verificando Pr√©-requisitos');

  const requirements = [
    { name: 'Node.js', command: 'node --version', minVersion: '18.0.0' },
    { name: 'npm', command: 'npm --version', minVersion: '8.0.0' },
    { name: 'Docker', command: 'docker --version', minVersion: '24.0.0' },
    { name: 'Docker Compose', command: 'docker-compose --version', minVersion: '2.0.0' }
  ];

  for (const req of requirements) {
    try {
      const output = execSync(req.command, { encoding: 'utf8' }).trim();
      log.success(`${req.name}: ${output}`);
    } catch (error) {
      log.error(`${req.name} n√£o encontrado. Por favor, instale a vers√£o ${req.minVersion} ou superior.`);
      process.exit(1);
    }
  }

  log.success('Todos os pr√©-requisitos foram atendidos!');
}

