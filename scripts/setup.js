async function installDependencies() {
  log.title('Instalando Depend√™ncias');

  const packages = [
    { name: 'Root', path: '.' },
    { name: 'Backend', path: 'backend' },
    { name: 'API Gateway', path: 'backend/api-gateway' },
    { name: 'User Management', path: 'backend/services/user-management' }
  ];

  for (const pkg of packages) {
    try {
      log.info(`Instalando depend√™ncias: ${pkg.name}...`);
      const pkgPath = path.join(__dirname, '..', pkg.path);
      
      if (fs.existsSync(path.join(pkgPath, 'package.json'))) {
        execSync('npm install', { cwd: pkgPath, stdio: 'inherit' });
        log.success(`${pkg.name}: Depend√™ncias instaladas`);
      } else {
        log.warning(`${pkg.name}: package.json n√£o encontrado, pulando...`);
      }
    } catch (error) {
      log.error(`Erro ao instalar depend√™ncias do ${pkg.name}: ${error.message}`);
    }
  }
}

